# Создадим переменную с радиусом круга
radius = int(input('Введите радиус круга:'))

# Зададим число Пи
pi = 3.14

# Найдём площадь круга, создав новую переменную для хранения величины площади круга.
square = pi * radius ** 2

# Выведем площадь круга с заданным радиусом
print('Площадь круга равна:', square)
